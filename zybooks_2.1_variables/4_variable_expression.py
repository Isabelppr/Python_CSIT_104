# Example 4: Variables in expressions
a = 5
b = 3
c = a + b  # Add a and b and store the result in c
print("Value of c (a + b):", c)  # Outputs 8

d = c * 2  # Multiply c by 2
print("Value of d (c * 2):", d)  # Outputs 16

# Explanation:
# - Variables can be used in mathematical expressions and formulas.
# - The value of a variable is substituted when the expression is evaluated.
