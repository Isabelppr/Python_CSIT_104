# Example 3: Incrementing a variable
x = 1
print("Initial value of x:", x)  # Outputs 1

x = x + 1  # Increment x by 1
print("Value of x after incrementing:", x)  # Outputs 2

x += 1  # Another way to increment x by 1
print("Value of x after another increment:", x)  # Outputs 3

# Explanation:
# - The `x = x + 1` or `x += 1` syntax is commonly used to increment a variable's value by 1.
# - This pattern is useful in loops, counters, and other iterative tasks.
