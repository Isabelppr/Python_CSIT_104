# Example 1: Basic variable assignment
x = 5
num_people = 10

print("Value of x:", x)  # Outputs 5
print("Number of people:", num_people)  # Outputs 10

# Explanation:
# - A variable like `x` or `num_people` is created and assigned a value using the `=` operator.
# - The variable can hold different types of data, like integers in this case.
