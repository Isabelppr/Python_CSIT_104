# Example 2: Reassigning a variable
x = 5
print("Initial value of x:", x)  # Outputs 5

x = 10  # Reassign a new value to the same variable
print("New value of x:", x)  # Outputs 10

# Explanation:
# - The value of `x` was first set to 5, and then reassigned to 10.
# - Variables can be reassigned new values at any point in the program.
