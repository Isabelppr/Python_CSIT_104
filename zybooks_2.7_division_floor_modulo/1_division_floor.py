# Example 1: Regular Division
print("Regular Division (5 / 2):", 5 / 2)  # Outputs 2.5

# Example 2: Floor Division
print("Floor Division (5 // 2):", 5 // 2)  # Outputs 2

# Explanation:
# - Regular division (/) returns a floating-point result.
# - Floor division (//) returns the largest integer less than or equal to the division result.
